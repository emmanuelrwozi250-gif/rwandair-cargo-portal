export const MONTHS = ['Jul-25','Aug-25','Sep-25','Oct-25','Nov-25','Dec-25','Jan-26','Feb-26','Mar-26','Apr-26','May-26','Jun-26'];

export const FY_TOTAL_TARGET = 33409034;

/** Returns monthly target for a given market */
export function getMarketTarget(marketName) {
  return REVENUE_TARGETS.find(m => m.market === marketName) || null;
}

/** Returns total FY target for a market */
export function getMarketFYTotal(marketName) {
  const m = getMarketTarget(marketName);
  return m ? m.total : 0;
}

/** Returns all markets sorted by FY total descending */
export function getTopMarkets(n = 10) {
  return [...REVENUE_TARGETS].sort((a,b) => b.total - a.total).slice(0, n);
}

/** Returns monthly totals across all markets */
export function getNetworkMonthlyTotals() {
  return MONTHS.map(m => ({
    month: m,
    target: REVENUE_TARGETS.reduce((sum, r) => sum + (r.monthly[m] || 0), 0)
  }));
}
