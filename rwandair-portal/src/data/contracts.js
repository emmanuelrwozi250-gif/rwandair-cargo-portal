// ── Utility Functions ────────────────────────────────────────────

export function getByType(type) {
  return CONTRACTS.filter(c => c.type === type);
}

export function getByStatus(status) {
  return CONTRACTS.filter(c => c.status === status);
}

export function getExpiringSoon(days = 90) {
  const now = new Date();
  const cutoff = new Date(now.getTime() + days * 86400000);
  return CONTRACTS.filter(c => {
    const exp = new Date(c.expiryDate);
    return exp >= now && exp <= cutoff && c.status !== 'expired';
  });
}

export function getTotalValue(contracts = CONTRACTS) {
  return contracts.reduce((sum, c) => sum + (c.value || 0), 0);
}

export function getByStation(station) {
  return CONTRACTS.filter(c => c.station === station);
}

export function daysUntilExpiry(contract) {
  const now = new Date();
  const exp = new Date(contract.expiryDate);
  return Math.ceil((exp - now) / 86400000);
}

export function getTypeInfo(typeId) {
  return CONTRACT_TYPES.find(t => t.id === typeId);
}

export function getStatusInfo(statusId) {
  return CONTRACT_STATUSES.find(s => s.id === statusId);
}
